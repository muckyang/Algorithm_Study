package Pass_Feb_Week02;

import java.util.Scanner;

public class Solution_전등켜기 {
	static int T;
	static int[] list;
	static int[] result;
	static int N;

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		T = sc.nextInt();
		for(int test_case =1 ; test_case <=T;test_case++) {
			N= sc.nextInt();
			int count =0;
			list= new int[N+1];
			result = new int [N+1];
			for(int i = 1 ; i <= N; i++) {
				list[i] =sc.nextInt();
				
			}
		
			for(int i = 1 ; i <= N;i++) {
				if(result[i] != list[i]) {
					count++;
					for(int j = i ; j <= N; j = j + i) {
						if(j > list.length)
							break;
						if(list[j] == 1)
							list[j]=0;
						else
							list[j]= 1;
					}
				}
			}
			System.out.println("#"+ test_case + " "+ count );
		}
	}
}


